li = [1, 3, 2, 7, 4]
print(li)
# sorts the list (assending order) #li.sort()
li.sort()
print(li)

# reverse the list (desending order) #li.reverse()
li.reverse()
print(li)

# add any number at the end of the list #li.append()
li.append(45)
print(li)

# insert any number at any position #li.insert(0,545)
li.insert(0,435)
print(li)

# remove number at any position #li.pop()
li.pop(2)
print(li)

# remove any number at the list #li.remove()
li.remove(435)
print(li) 